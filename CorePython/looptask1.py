# Write a for loop to print numbers from 10 to 1. All numbers should be in separate lines.
l=1
h=10
print("printing number from 1 to 10")
for i in range(l,h+1):
    print(i)
print("printing number from 10 to 1")
for i in range(h,0,-1):
    print(i)